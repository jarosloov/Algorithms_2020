
# Программа которая меняет местами переменные. #

# создаем переменные.
a = 6
b = 3 
# Выводим переменные.
print("Мы ввели значения:")
print ("a =", a)
print ("b =", b)
print("Меняем местами значения переменых.")
# Примеменяем секретный магический прем по замене переменых :) 
c = a
a = b
b = c
# Выводим новые значения переменых.
print("Новые значения:")
print ("a =", a)
print ("b =", b)
# Опа, они и правда поменялись местами.
